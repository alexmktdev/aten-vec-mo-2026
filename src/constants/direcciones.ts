export type DireccionMunicipalKey = keyof typeof DIRECCIONES_MUNICIPALES;

export const DIRECCIONES_KEYS = Object.keys(DIRECCIONES_MUNICIPALES) as DireccionMunicipalKey[];

export function getDireccionLabel(key: string): string {
  const dir = DIRECCIONES_MUNICIPALES[key as DireccionMunicipalKey];
  return dir ? dir.label : key;
}

export function getCategorias(key: string): readonly string[] {
  const dir = DIRECCIONES_MUNICIPALES[key as DireccionMunicipalKey];
  return dir ? dir.categorias : [];
}

export function isValidDireccion(key: string): key is DireccionMunicipalKey {
  return key in DIRECCIONES_MUNICIPALES;
}

export function isValidCategoria(direccionKey: string, categoria: string): boolean {
  const categorias = getCategorias(direccionKey);
  return categorias.includes(categoria);
}
